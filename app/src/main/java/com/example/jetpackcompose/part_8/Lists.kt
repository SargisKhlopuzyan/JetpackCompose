@Composable
fun FunLists() {
    val scrollState = rememberScrollState()
    Column(
        modifier = Modifier.verticalScroll(scrollState)
    ) {
        for (i in 1..50) {
            Text(
                text = "Item $i",
                fontSize = 24.sp,
                fontWeight = FontWeight.Bold,
                textAlign = TextAlign.Center,
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(vertical = 24.dp)
            )
        }
    }
}

@Composable
fun FunLists_LazyColumn(myViewModel: MyViewModel) {

    //        mutableStateOf<List<String>>(listOf())

    var listsState = remember {
        myViewModel.names
    }

    LazyColumn {
        itemsIndexed(
            items = listsState
//            listOf("This", "is", "Jetpack", "Compose")
        ) { index, string ->
            Text(
                text = string,
                fontSize = 24.sp,
                fontWeight = FontWeight.Bold,
                textAlign = TextAlign.Center,
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(vertical = 24.dp)
            )
        }

        items(50) {
            Text(
                text = "Item $it",
                fontSize = 24.sp,
                fontWeight = FontWeight.Bold,
                textAlign = TextAlign.Center,
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(vertical = 24.dp)
            )
        }
    }

//    listsState = listOf("This", "is","Jetpack", "Compose")

    myViewModel.delayAndChange()
}

